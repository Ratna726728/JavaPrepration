package Day02;

public class WhileLoop {
    public static void main(String[] args) {
        System.out.println("increment");
        int i=1;
        while(i<=10){
            System.out.println("value of i>>"+i);
            i++;
        }
        System.out.println();
        System.out.println("decrement");
        int j=10;
        while(j>0){
            System.out.println("value of j>>"+j);
            j--;
        }
    }
}
