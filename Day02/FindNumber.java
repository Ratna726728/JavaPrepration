package Day02;

public class FindNumber {
    public static void main(String[] args) {
        int num=2;
        switch (num){
            case 1:
                System.out.println("this is number 1");
                break;

            case 2:
                System.out.println("this is number 2");
                break;

            case 3:
                System.out.println("this is number 3");
                break;

            default:
                System.out.println("invalid input");
        }
    }
}
