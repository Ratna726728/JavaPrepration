package Day01;

import java.security.KeyStore;

public class PrimitiveDataType {
    public static void main(String[] args) {
        byte a = 10;
        System.out.println(" value of a is " + a+" which is a primitive data type");
        short b = 100;
        System.out.println(" value of b is "+b+" which is a primitive data type");
        int c = 1024;
        System.out.println(" value of c is "+c+" which is a primitive data type");
        long d = 10L;
        System.out.println(" value of d is "+d+" which is a primitive data type");
        float e = 10.5f;
        System.out.println(" value of e is "+e+" which is a primitive data type");
        double f = 10.25d;
        System.out.println(" value of f is "+f+" which is a primitive data type");
        char g = '#';
        System.out.println(" value of g is "+g+" which is a primitive data type");
        boolean h = true;
        System.out.println(" value of h is "+h+" which is a primitive data type");
    }
}
